def _web_index_path() -> Path:
    """Return the packaged or source Web Companion index.html path."""
    candidates = []

    # Normal source/dev path.
    candidates.append(Path(__file__).resolve().parent / "static" / "index.html")

    # PyInstaller one-file extraction path.
    mei = getattr(sys, "_MEIPASS", None)
    if mei:
        candidates.append(
            Path(mei) / "fzastro_ai" / "web_companion" / "static" / "index.html"
        )

    # PyInstaller/current working directory fallback.
    candidates.append(
        Path.cwd() / "fzastro_ai" / "web_companion" / "static" / "index.html"
    )

    for candidate in candidates:
        if candidate.exists():
            return candidate

    # Return first candidate so the error message is still useful.
    return candidates[0]


