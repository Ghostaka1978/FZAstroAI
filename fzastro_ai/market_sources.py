def _stock_number(value):
    """Convert finance API numbers or formatted strings to float."""
    if isinstance(value, (int, float)):
        return float(value)

    clean = re.sub(r"[^0-9+\-.]", "", str(value or "").strip())

    if clean in {"", "+", "-", ".", "+.", "-."}:
        return None

    try:
        return float(clean)
    except (TypeError, ValueError):
        return None


def _stock_market_state(meta):
    raw_state = str(meta.get("marketState") or "").strip().upper()

    state_names = {
        "REGULAR": "Open",
        "PRE": "Pre-market",
        "PREPRE": "Pre-market",
        "POST": "After hours",
        "POSTPOST": "After hours",
        "CLOSED": "Closed",
    }

    if raw_state in state_names:
        return state_names[raw_state]

    current_time = time.time()
    periods = meta.get("currentTradingPeriod") or {}

    for key, label in (
        ("pre", "Pre-market"),
        ("regular", "Open"),
        ("post", "After hours"),
    ):
        period = periods.get(key) or {}
        start = _stock_number(period.get("start"))
        end = _stock_number(period.get("end"))

        if start is not None and end is not None and start <= current_time < end:
            return label

    return "Closed"


def _format_stock_epoch(value):
    timestamp = _stock_number(value)

    if timestamp is None:
        return "Unavailable"

    try:
        return datetime.fromtimestamp(timestamp, tz=timezone.utc).strftime(
            "%Y-%m-%d %H:%M:%S UTC"
        )
    except (OSError, OverflowError, ValueError):
        return "Unavailable"


def _format_stock_quote_markdown(
    ticker,
    company_name,
    price,
    currency,
    change,
    percentage_change,
    market_status,
    quote_timestamp,
    exchange,
    source_name,
    source_url,
    retrieved_at,
):
    """Serialize a structured quote for the dedicated market-card renderer."""
    payload = {
        "ticker": str(ticker or "").strip().upper(),
        "company_name": str(company_name or ticker or "Market quote").strip(),
        "price": _stock_number(price),
        "currency": str(currency or "USD").strip().upper(),
        "change": _stock_number(change),
        "percentage_change": _stock_number(percentage_change),
        "market_status": str(market_status or "Unavailable").strip(),
        "quote_timestamp": str(quote_timestamp or "Unavailable").strip(),
        "exchange": str(exchange or "Unavailable").strip(),
        "source_name": str(source_name or "Source").strip(),
        "source_url": str(source_url or "").strip(),
        "retrieved_at": str(retrieved_at or "Unavailable").strip(),
    }

    return "[STOCK_QUOTE]\n" + json.dumps(
        payload, ensure_ascii=False, separators=(",", ":")
    )


def parse_stock_quote_payload(text):
    clean_text = str(text or "").strip()

    if not clean_text.startswith("[STOCK_QUOTE]"):
        return None

    json_text = clean_text[len("[STOCK_QUOTE]") :].strip()

    if not json_text:
        return None

    try:
        payload = json.loads(json_text)
    except (TypeError, ValueError, json.JSONDecodeError):
        return None

    if not isinstance(payload, dict):
        return None

    ticker = str(payload.get("ticker") or "").strip().upper()

    if not ticker:
        return None

    payload["ticker"] = ticker
    return payload


